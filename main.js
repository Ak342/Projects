

/* For First One */

document.getElementById("clickevent1").addEventListener("click",function(){
	let x = document.getElementById("skillset1");
	
	if(x.style.display === "none"){
		x.style.display = "block";
	}
	else{
		x.style.display = "none";
	}
});


